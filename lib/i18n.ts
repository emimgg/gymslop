export const translations: Record<Lang, Record<string, string>> = { es, en };

// ─── Simple interpolation: t('key', { n: 5 }) replaces {{n}} ─────────────────
export function makeT(lang: Lang) {
  return function t(key: string, vars?: Record<string, string | number>): string {
    const raw = translations[lang][key] ?? translations['en'][key];
    // For 'ex.*' keys with no translation (custom exercises, unseeded DB):
    // strip the prefix and return the stored name as-is rather than exposing the key.
    const str = raw ?? (key.startsWith('ex.') ? key.slice(3) : key.startsWith('food.') ? key.slice(5) : key);
    if (!vars) return str;
    return str.replace(/\{\{(\w+)\}\}/g, (_, k) => String(vars[k] ?? ''));
  };
}
